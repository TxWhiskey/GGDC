            <div class="text-section">

                <p class="blog-text">
                    <b>Q</b>: How involved are your clients during the decision making of finishes and furnishings?
                </p>
                <p class="blog-text">
                    <b>A</b>: It really depends on the client. I would say more often than not, clients are generally pretty involved. I think the type of person that typically reaches out to a designer to help them with their home falls into one of two categories. First, they are design enthusiasts who love everything about design and love the process, so they can be very involved and have opinions and obviously their own taste that they bring to the table, and they love to be very collaborative. And we really enjoy that type of client. And then there's also the type of client that is extremely busy, and as a result, doesn't have the time or necessarily the interest in being quite as involved and really sort of trusts us to lead the way and bring the vision to life. That type of client is a little bit rarer. But it's also really nice to work with them because if they're trusting of the vision and the process, then they're allowing you the creative freedom to really do your best work.
                </p>
                <p class="blog-text">
                    <b>Q</b>: What ignites your creativity?
                </p>
                <p class="blog-text">
                    <b>A</b>: Oh, that's a great question. And it really depends. Pre COVID, it was always travel, just getting out of my regular routine and allowing my eyes to see different things. In fact, we were just in the mountains in North Carolina last week and the light was so beautiful through the trees. It's little things like that from nature that are super inspiring. But when you're immersing yourself in a different culture, different location, there's so many things that you can derive inspiration from like the architecture, the food, the people, and the history. So, I feel like just getting out of your routine is always helpful for inspiration. But these days, with a little baby at home and less travel with the pandemic, I would say I often delve into books. I go back to some of my favorite coffee table books and just look through them with a fresh set of eyes and pick up on different details that I may not have noticed before.
                </p>

            </div>

        </div>
    </div>

    <div class="row">
