        </div>
    </div>

    <!-- Section 2 -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    <b>Q</b>: Do you plan everything out from the beginning? Or do you take it piece by piece?
                </p>
                <p class="blog-text">
                    <b>A</b>: It depends on the project. If we get in early enough, where the architect has just put pencil to paper, and we have time before they break ground, that's the best-case scenario, because then we have the luxury of time. And as the architect is developing, we can formulate the interior plan and we can put together all of the different finishes and fixtures for the client to see as a package so they can see the full vision. But more often than not, either the plans are already done, or in some cases, for instance, right now, we're working on a new construction where the clients bought it as a spec house. And so, construction is already underway, and we're still able to make all of our new selections, but because construction is 40% of the way through at this point, we're having to really make our selections very quickly. So that's a situation where we aren't necessarily able to present the full picture and show the client tile, plumbing fixtures, and countertops all together. We're selecting things as they're needed based on the builder’s needs. And then, as we make one selection and move on to the next, we'll refer back to what's already been chosen so the client can start to formulate that vision and see how things relate to one another.
                </p>

            </div>

        </div>
