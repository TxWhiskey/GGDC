                <p class="blog-text">
                    <b>Q</b>: How many projects do you work on at any given time?
                </p>
                <p class="blog-text">
                    <b>A</b>: On average, we're somewhere between 12 and 15 projects, and they're all at different stages, it just depends on when we started them. And as I mentioned before, the scope is generally the full house, whether that's just a furnishings project, or, more often than not, a renovation or new construction where we're doing the entirety of the house. 
                </p>

                <p class="blog-text">
                    <b>Q</b>: With so many projects at one time, who does your team consist of? 
                </p>
                <p class="blog-text">
                    <b>A</b>: It's a lot of projects. So, we have a really wonderful and dynamic team. We have two design associates, and each project is assigned a design associate that works in tandem with me on the project with the client. And then we have a really wonderful design assistant who helps both of those teams with technical needs such as elevations and drawings. And we also have an office manager who handles the business end of things.
                </p>

