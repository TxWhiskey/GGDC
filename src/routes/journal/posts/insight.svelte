            <div class="text-section">

                <p class="blog-text">
                    <b>Q</b>: How do you sequence the order of selections for new construction and remodel projects? Is this something established by the builder? Or do you have a system in place that you like to follow?
                </p>
                <p class="blog-text">
                    <b>A</b>: Definitely both. So, every builder has different processes and sort of a different speed at which they work. So, what we like to do when we're kicking off a major renovation or new construction project is have a meeting with the entire team. And oftentimes, that's the architect, the builder and the designer, so that we can get on the same page and understand what is required of us and what those timelines look like. From there, we put everything into schedules. It depends what that particular project calls for in terms of the project manager and the builder and which finishes or selections they need. First, more often than not, you're looking at big structural things like windows and flooring. And then pretty soon after that, plumbing fixtures because they have to rough them in. So that's usually pretty early on. And then after that, as work gets further along, we're looking at tile, cabinetry, paint colors, lighting, and cabinet hardware.
                </p>

            </div>

        </div>
    </div>

    <div class="row">
