                <p class="blog-text">
                    <b>Q</b>: What is your go to white, black/bronze and accent color paint?
                </p>
                <p class="blog-text">
                    <b>A</b>: Our go-to white that we use in many projects is <b>Benjamin Moore white dove</b>. And the reason I like that particular color is because it has sort of a warm, neutral undertone. It doesn't pull to yellow or to pink, it really is sort of the perfect neutral base, but it has a nice warmth to it. So, it works in just about any space. I have yet to find a project where it wasn't the right shade of white. For black, what we have used recently is <b>Iron Ore</b>, and I forget if that's Sherwin Williams or Benjamin Moore, but we've used that color for a couple of projects recently. And it actually is a really great color not only for exterior trim, but also interior doors. It's really pretty black or almost black. For an accent paint color would say I love sort of a moody color. <b>Benjamin Moore Hale Navy</b> is a really beautiful color. It looks great in bedrooms and libraries. And I also really like <b>Down Pipe by Farrow and Ball</b> as well as <b>Hague blue</b>. They're really concentrated and have beautiful undertones. 
                </p>

            </div>

        </div>
    </div>

