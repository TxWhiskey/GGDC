            <h3 class="section-header">
                My first interview is with award-winning interior designer <a class="header-link" href="https://www.palomacontreras.com/" target="_blank">Paloma Contreras</a> of Paloma Contreras Interior Design and Paloma & Co. 
            </h3>

            <p class="blog-text">
                "Delivering a modern take on traditional style– gravitating towards classic silhouettes and timeless pieces paired with a touch of glamour and an infusion of color."
