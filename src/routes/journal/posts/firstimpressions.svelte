            <h3 class="section-subheader">1. Preparing an Itemized Proposal</h3>

        </div>
    </div>


    <!-- Section 2A -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    This means your budget should contain close to 100 individual items with a cost and label associated with each one. Each item should be identified as a service, material, labor, or labor and material.
                </p>
                <ul class="blog-list">
                    <li class="blog-list-item">
                        <p class="blog-text">
                            <b>Services</b>: these can be services rendered by a third party, such as a Sustainability Consultant
                        </p>
                    </li>
                    <li class="blog-list-item">
                        <p class="blog-text">
                            <b>Material</b>: meaning the material will be purchased directly by the builder with a manufacturer or vendor and will be handed to the labor tradesman crew for installation
                        </p>
                    </li>
                    <li class="blog-list-item">
                        <p class="blog-text">
                            <b>Labor</b>: the builder might choose to remove the material responsibility from a tradesman and will execute a contract solely for the labor.
                        </p>
                    </li>
                    <li class="blog-list-item">
                        <p class="blog-text">
                            <b>Labor and Material</b>: other times, are executed as an all-inclusive (turnkey) work to one tradesman
                        </p>
                    </li>
                </ul>
                <p class="blog-text">
                    It’s important to identify the items according to these categories so that as a client, you have an idea about what your material cost is and how you can reduce it (because that is something you have control over). Allowing you to look at alternative options if material costs are too high for a particular line item.
                </p>                
                <p class="blog-text">
                    This brings me to my next point...
                </p>                
            </div>

        </div>
    </div>

