    <!-- Section 2A SubHeader -->
    <div class="row">
        <div class="col">

            <h3 class="section-subheader">1. Lending Institution or Officer</h3>

        </div>
    </div>


    <!-- Section 2A -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    Suppose you’re thinking of obtaining a loan for building or renovating your home or want to cover a portion of the cost through financing. In that case, it’s a good idea to start searching for which financial institutions to consider. 
                </p>
                <p class="blog-text">
                    After you do your research, you can go through the pre-approval phase to see whether you’re eligible for the loan and what you need to do to get to the next step.
                </p>
                <p class="blog-text">
                    It’s important to note that this is only the pre-approval stage; you will be able to fill out the actual loan application after you’ve worked out your architectural and structural plans that the housebuilder in Texas can use to provide a construction budget. You’ll have to submit this budget to the lending institution along with your loan application.
                </p>                
                <p class="blog-text">
                    Ideally, you should start thinking about what your family will be comfortable spending and how much financing you’re going to need. There is a lot to consider here, including the cost of the lot, down payment, monthly mortgage payment, and so on. Keep in mind that certain expenses, such as the builder’s deposit, are not covered in the construction loan, so you’ll have to keep a separate budget for that.
                </p>                
            </div>

        </div>
    </div>

    <!-- Section 2B SubHeader -->
    <div class="row">
        <div class="col">

            <h3 class="section-subheader">2. Realtor</h3>

        </div>
    </div>

    <!-- Section 2B Image -->
    <div class="row">
        <div class="col">
            <img class="blog-image" src="/blogs/gettingstartedchecklist/Foundation.JPG" alt="We take pride in connecting you to a reliable realtor who will help you find the best homes for sale in Houston, secure a good deal, and take care of all of the necessary paperwork.">
        </div>
    </div>

    <!-- Section 2B -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    Whether you’re building a new home on a cleared lot or buying an existing home that you’ll eventually renovate, you’re going to need a realtor who can filter out the best options from all the potential choices.
                </p>
                <p class="blog-text">
                    An honest and reliable realtor will ensure that you find a lot or home that meets all your requirements and your budget. They’ll work to secure and close a good deal and will take care of all the necessary paperwork on your behalf.
                </p>
                <p class="blog-text">
                    Steer clear of any realtor who puts you directly in contact with the builder. A lot of times, realtors partner up with builders who offer a commission in return for referrals, leading to a bias. However, it’s common practice for realtors to request a fee less than or equal to 3% of the construction cost if the builder they suggest is hired.
                </p>          
            </div>

        </div>
    </div>
 
    <!-- Section 2C SubHeader -->
    <div class="row">
        <div class="col">

            <h3 class="section-subheader">3. Builder</h3>

        </div>
    </div>

    <!-- Section 2C -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    Choosing the right builder (or construction contractor) for your home is critical, as they can either turn your vision into a reality or leave you with a poorly built home. 
                </p>
                <p class="blog-text">
                    During the pre-construction phase. A builder’s job is to:
                </p>
                <ul class="blog-list">
                    <li class="blog-list-item">Produce a preliminary budget, then a refined construction budget</li>
                    <li class="blog-list-item">Offer value engineering, or in other words, cost saving initiatives</li>
                    <li class="blog-list-item">Submit for HOA approval and city or county permitting</li>
                    <li class="blog-list-item">Procure a tentative construction schedule</li>
                    <li class="blog-list-item">Work with both the architect and interior designer to review constructability design intent</li>
                    <li class="blog-list-item">Coordinate with the interior designer on prioritization of selections</li>
                </ul>
                <p class="blog-text">
                    There is a lot more that a builder does but that falls in the actual construction phase. Custom home builders in Houston can charge $5,000-$10,000 separately for the pre-construction services.  This cost is usually applied to the construction cost if the builder is hired.
                </p>            
            </div>

        </div>
    </div>

    <!-- Section 2C Image -->
    <div class="row">
        <div class="col">
            <img class="blog-image" src="/blogs/gettingstartedchecklist/PatriciaAndBob.jpg" alt="Planning to obtain a loan to cover the whole cost estimate  - or even a part of it - provided by a housebuilder in Texas? Start searching for financial institutions to consider here… ">
        </div>
    </div>

    <!-- Section 2C-2 -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    Typically, construction contractors in Houston, TX offer two types of contracts: either a cost-plus fee contract or a fixed-price contract. In the former, the builder’s fee can vary between 10-30% of the cost, depending on whether you choose a builder who works on an individual basis or an established builder with a team. Renovations typically run at a higher fee because there’s a lot of uncertainty about the cost.
                </p>
                <p class="blog-text">
                    Most home builders in Texas prefer cost-plus contracts as they eliminate most of the risk, while in the case of fixed-price contracts, the builder carries most of the risk. When a builder offers a fixed price contract, they’ll probably include a discretionary contingency lump sum to help mitigate any budget busts or unforeseen circumstances.
                </p>
                <p class="blog-text">
                    You should know that if you move forward with a fixed-price contract, the books will be closed and any savings will go to the builder (except for established allowance items).
                </p>            
            </div>

        </div>
    </div>

    <!-- Section 2C-2 Image -->
