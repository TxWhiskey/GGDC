    <div class="row">
        <div class="col">

            <h3 class="section-subheader">4. Architect</h3>

        </div>
    </div>

    <!-- Section 3 -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    In order for the builder to put together a preliminary or construction budget they will need the architectural and structural drawings.  
                </p>
                <p class="blog-text">
                    An architect’s job is to:
                </p>
                <ul class="blog-list">
                    <li class="blog-list-item">Learn the needs of each household member and their architectural preferences</li>
                    <li class="blog-list-item">Generate a concept sketch for the house</li>
                    <li class="blog-list-item">Create CAD drawings for site plan, floor plan, exterior, and interior elevation</li>
                </ul>
                <p class="blog-text">
                    Custom home architects in Houston typically charge between $2/sq. ft to $5/sq. ft framed area (including garages and porches) to produce architectural plans. This fee doesn’t include irrigation, landscape, audio and visual, permitting, and HOA submittal.
                </p>
                <p class="blog-text">
                    You’ll need a Landscape Designer to take care of the irrigation and drainage system plans, and landscape plans with a materials and specifications list, which will cost you another $2,000-$10,000 depending on the scope of the project.
                </p>
            </div>

        </div>
    </div>
 
    <!-- Section 4 SubHeader -->
    <div class="row">
        <div class="col">

            <h3 class="section-subheader">5. Structural Engineer</h3>

        </div>
    </div>

    <!-- Section 4 -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    A structural engineer’s responsibilities include providing foundation drawings along with framing plans and details with windstorm consideration. They typically charge about $0.65/sq. ft to $0.90/sq. ft. 
                </p>
                <p class="blog-text">
                    In order to do this work, the structural engineer will need help from a soil testing company that’ll test the soil. This test will help the engineer determine the foundation needed based on the ground conditions.
                </p>
                <p class="blog-text">
                    Soil testing itself costs anywhere between $1,200 and $2,400. On average you can expect to pay around $600 a bore.
                </p>
            </div>

        </div>
    </div>
 
    <!-- Section 5 SubHeader -->
    <div class="row">
        <div class="col">

            <h3 class="section-subheader">6. Interior Designer</h3>

        </div>
    </div>

    <!-- Section 5 -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    The architect, builder, and interior designer work simultaneously on the home’s design. It's important that all three collaborate with each other during the preconstruction phase when possible.  The interior designer will make their magic and turn your house into a home. 
                </p>
                <p class="blog-text">
                    An interior designer will:
                </p>
                <ul class="blog-list">
                    <li class="blog-list-item">Provide design inspiration and concepts</li>
                    <li class="blog-list-item">Advice on spacing and layout modifications</li>
                    <li class="blog-list-item">Work off the architect’s CAD elevations to produce detailed interior elevations</li>
                    <li class="blog-list-item">Ensure furniture placement and sizing is taken into consideration</li>
                </ul>
                <p class="blog-text">
                    The fee of interior designers in Houston usually ranges between $90/hour to $200/hr with a minimum of about $7,000 retainer. This isn’t inclusive of material or furnishings going into the home.
                </p>
                <p class="blog-text">
                    It’s also important to note that the fee charged by these parties is typically not covered in a construction loan unless you roll it under the builder's responsibility to contract these services out. If you choose to do that, be prepared to pay a fee to the builder on top of each professionals’ price. This can be anywhere from 10% to 30% more depending on the type of construction, contract type, and magnitude of the scope of work.
                </p>
            </div>

        </div>
