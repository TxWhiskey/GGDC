            <div class="text-section">

                <p class="blog-text">
                    <b>Q</b>: How does your team stay organized?
                </p>
                <p class="blog-text">
                    <b>A</b>: Well, we are a small staff by design. So, we work together quite a bit. One of my favorite parts of the week is Monday morning. We have a weekly staff meeting, and we go through our calendars, we have an inspiration photo, and everyone looks for something that just catches their eye. That's a lot of fun. We look at that together, and we study the photo when we talk about it. We learn a design tip from ourselves pretty much every week. But also, we go over our calendar, we go over everything that's on the books for clients, and we go over every single project, one by one, talking about where we are, what's next, and what needs to be done. Most of the work has already been assigned to one of us, but at that point, we might assign small tasks to somebody, or, for example, one of my designers is particularly good at fabrics and textiles, so if all of a sudden, we're ready to shop for fabrics, then maybe she might take that part of the project on for that client. So, it's staying in touch, keeping binders and folders. And also, we do a lot of work on PowerPoint. When decisions are made, we usually put a picture, a link, and any other specific information like dimensions, or what the layout is going to be. We keep all of that in a PowerPoint. I know most contractors have their own way, and you were very good. You had documents that you wanted filled out because you had your way of doing it. But for us, visuals are very important because when we're shopping with that client down the road for a rug or a lamp, it's so great to have basically a picture of everything in the house, and PowerPoint is a really easy way to do that. Everyone can look at it, they can edit it, they can add things, and subtract things. So, we have a living design plan, if you will, that we take with us to meetings and we build that while working with the client and at the end, the builder.
                </p>

