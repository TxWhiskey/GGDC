            <div class="text-section">

                <p class="blog-text">
                    <b>Q</b>: How closely do you work with Builders and Architects when working on a large scale renovation or new build home? What are some of the major elements needing coordination between all parties?
                </p>
                <p class="blog-text">
                    <b>A</b>: Well, we like to work with them as closely as possible. We’re really good team players, and from what I can tell, all of our builders and architects have really liked working with us because we do what we say we’re going to do, we do it on time, and we’re very clear with our communications. And I was glad to hear that you liked working with designers because there are some (designers) who, I think, have maybe made jobs harder than they had to be, or made things hard on the builder, but we try to be the opposite. We try to keep the client in line with selections, allowances, and all of the processes that basically the designer and the client are responsible for. We like to stay in close communication so that we’re finished with them in advance of when the architect or the builder needs them. 
                </p>

            </div>

        </div>
    </div>

