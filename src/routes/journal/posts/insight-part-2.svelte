    <!-- Question 3 -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    <b>Q</b>: Where do you like to focus your resources within the budget?
                </p>
                <p class="blog-text">
                    <b>A</b>: You know, it's hard to specify that. All parts of the project are really important, but we like to finish, and sometimes that's a struggle. Sometimes building gets out of hand and it gets very expensive, or other things happen in life. And a project can always be finished to the level of completion that we would like to see: the client happy in their home with everything they need in that home. And so, I like to make sure we save some money at the end for the design and the decor. Because if you have the best poured foundation on the planet, but you don't have a comfortable sofa, you're never going to be that happy with the house. So, for us, it's very important, while we get deeply involved in the construction side, I really like to get to finish with the art, the rugs, the sofas, the furniture, and the accessories because to me, that's what makes the house a home.
                </p>
