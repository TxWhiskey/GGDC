                <p class="blog-text">
                    <b>Q</b>: What does a residential full-service package translate to for Pamela Hope Designs?
                </p>
                <p class="blog-text">
                    <b>A</b>: Well, we like to say we take you from paint colors to sofa pillows, and we love when we can be involved with the entire design process. We’ll start with the concept, and sometimes a client comes to us and we help them find an architect, we help them find a builder, we help them find the various trades and services. Other times, a contractor or an architect will refer to us, so some of that work may have been done. The drawings may have already been done and the house has already been designed, but we like to get involved as soon as possible either in pre-construction or as soon as possible afterwards so that we can help with the selections and be a team member the whole way.  
                </p>

