                <p class="blog-text">
                    <b>Q</b>: Lessons learned?
                </p>
                <p class="blog-text">
                    <b>A</b>: Set very clear ground rule expectations from the beginning to make sure the builder is a good fit.  It will be different for each person, but I should have made it clear that I wanted a truly custom home, down to every detail.  My expectation was that these “details” were assumed to be part of the “custom” build process, but the builder often assumed the details would fall into place at a later time, which led to unsatisfactory results on certain parts of the home. 
                </p>
                <p class="blog-text">
                    <b>Q</b>: Advice to anyone who is about to embark on this homebuilding journey.
                </p>
                <p class="blog-text">
                    <b>A</b>: Even with a reputable builder, it will very likely take a LOT of time and effort to QC everything and ensure the builder is working on your behalf.  It’s a business and they will do what’s best for them.  This client representative niche is a necessary one that would have saved some of my sanity and probably will end up saving the homeowner more $$ than it costs.
                </p>
            </div>
            <h3 class="section-subheader">Summary</h3>
            <div class="text-section">

                <p class="blog-text">
                    Again, building a home is a huge investment. For many, it’s the largest sum of money you’ll spend in your lifetime. Adding another expense to the pile may sound unnecessary at first, but having a home construction advisor in your corner can actually save you more money than it costs to hire one.

                </p>
                <p class="blog-text">
                    If you’re just starting your home building journey in Houston, TX, consider hiring a residential construction consultant. Having an experienced advocate on your side will make the process much less stressful. 

