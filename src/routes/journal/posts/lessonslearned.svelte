    </div>

    <!-- Section 4 -->
    <div class="row">
        <div class="col">
            <div class="text-section">

                <p class="blog-text">
                    <b>Q</b>: Were expectations met?
                </p>
                <p class="blog-text">
                    <b>A</b>: This is a loaded question.  Overall, our expectations of the builder were not met.  We ended up with a beautiful home that we love, but we feel that a lot of credit for that goes to the countless hours and nit-picking we put into the process. 
                </p>
                <p class="blog-text">
                    <b>Q</b>: Was there a recurring meeting set in place to review your project progress?
                </p>
                <p class="blog-text">
                    <b>A</b>: At the beginning (pre-construction) there was a pretty regular meeting cadence, then it was more ad-hoc as construction got going.  I think I would have benefitted from a regularly scheduled meeting with the team rather than feeling I had to request meetings all the time.
                </p>
                <p class="blog-text">
                    <b>Q</b>: Did you ever feel like you had to defend your point or perspective on things?
                </p>
                <p class="blog-text">
                    <b>A</b>: Yes, I think the custom home builder should work to understand and align on each individual homeowner’s vision.  I don’t feel like my builder did that on several details of my home, which caused re-work and dis-satisfaction on both sides.
                </p>
                <p class="blog-text">
                    <b>Q</b>: Were you ever overwhelmed with the jobsite progress?
                </p>
                <p class="blog-text">
                    <b>A</b>: Yes, there are infinite details and decisions in the custom home construction process.  I often felt overwhelmed with all the moving parts, and I was nervous at the incompetence/lack of experience of some of the construction team. 
                </p>
                <p class="blog-text">
                    <b>Q</b>: As you received invoices to be paid did you look through every single invoice?  Did you ever say to yourself ‘what the heck am I looking at’?  Did you dispute any invoices that came through?
                </p>
                <p class="blog-text">
                    <b>A</b>: I tried!  There were SO many… hundreds and hundreds of pages, many with short-hand or unclear descriptions.  I questioned a few and got good feedback from my PM and accountant, but with the pure volume of work going on, I’m sure things were missed.
                </p>
            </div>
            <h3 class="section-subheader">Summary</h3>
            <div class="text-section">

                <p class="blog-text">
                    It’s important to discuss your expectations with your builder and vice versa. That way, you can go into your home construction confident that your builder knows what you expect throughout your project and as an end result. And, when it comes to invoices, you should have an expert review them, someone who doesn’t profit by you overpaying or double paying.  Let’s ensure you are covered on your end.  
                </p>
            </div>
        </div>
