<script lang='ts'>
import type { FolderStructure } from '$lib/types/files';


    import { getContext } from 'svelte'
    import type { Writable } from 'svelte/store';
    import type { FileExplorerView } from './file-explorer-types';

    const view: Writable<FileExplorerView> = getContext('View')

    let newFolderName: string = ''

    async function goToFolder( index: number ) {

        let pathIds: string[] = $view.path.map( p => p.id )

        pathIds.splice( index + 1 , pathIds.length )

        $view.path.splice( index + 1, $view.path.length)

        $view.folderLoading = true

        const res = await fetch( '/api/files', {
            method: "POST",
            headers: {
                "Content-Type": "application/json"
            },
            body: JSON.stringify({
                path: pathIds
            })
        })

        let folderStructure: FolderStructure = await res.json()

        view.update( v => {
            v.files = folderStructure.files
            v.folders = folderStructure.folders
            v.path = folderStructure.path
            v.selectedFileId = null
            v.folderLoading = false

            return v
        })

    }

    async function deleteFileOrFolder( targetId: string ) {

        const res = await fetch( '/api/files/delete/' + targetId, { method: "DELETE" })

        if ( res.ok ) {

            const body = await res.json()

            if ( body.type == 'File' ) {

                const fileIndex = $view.files.findIndex( file => file.id == body.targetId)

                $view.files.splice(fileIndex, 1)

                /* $view = $view */

                $view.selectedFileId = null

            }

            if ( body.type == 'Folder' ) {

                $view.path.pop()

                goToFolder( $view.path.length - 1 )

            }
            
        }

    }

